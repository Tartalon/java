public class C08_TwoDimentionalArray {
    public static void main(String[] args) {
        int[] vector = new int[1];
        vector[0] = 1;
        System.out.println(vector[0]);

        int[][] matrix = new int[1][1];
        matrix[0][0] = 200;
        System.out.println(matrix[0][0]);
    }
}
